public class Comodo {
    private String nome;
    private double tamanhoM2;

    public Comodo(String nome, double tamanhoM2){
        this.nome = nome;
        this.tamanhoM2 = tamanhoM2;
    }

    public String getNome() {
        return nome;
    }

    public double getTamanhoM2() {
        return tamanhoM2;
    }

}
